<span style="font-size:17pt"><b>Explanation</b></span><br />
<table class="smalltabl"><tr><td><span class="expla">CrowdForce tribute is a sum of money, like a bounty,<br />
that you're willing to pay to get the thing you want,<br />
whether it's an apartment, a job, or a slightly-used unicycle.<br />
But a "bounty" typically goes to one person. A CrowdForce tribute can be<br />
divided up among a whole crowd of people so that everyone has an<br />
incentive to help you find what you're looking for.<br /><br />

So how does it work?<br /><br />

Let's take an example. Say Matt is looking for a one-bedroom apartment in New York,
<br />and he's willing to pay $100 total to the person or people who<br />
can help him find it. Matt creates a $100 CrowdForce Challenge.<br />
The service then gives Matt a url that he can share with
<br />friends over email or his social networks.<br /><br />

Now let's say that Matt sends the url for his Challenge<br />
to his friend Amanda. When Amanda clicks on the link, she<br />
arrives at a CrowdForce page and sees two options:<br /><br />

Option One:<br />
If Amanda thinks she knows of an apartment for Matt,<br />
she'll be able to email Matt via a unique email
<br />address provided by the service and claim the money.<br /><br />

Option Two:<br />
If Amanda doesn't know of any apartments, she can still help --<br />
and share in the CrowdForce -- by choosing to spread word about
<br />Matt's apartment search with her own network. When Amanda
<br />clicks one of the share buttons for Facebook, Twitter, or email,
<br />she'll receive a unique url. This url is unique so that the
<br />service can trace Amanda's role in the process and reward her
<br />once Matt finds his apartment. When Amanda's friends receive
<br />Amanda's link, they'll face the same options as Amanda.
<br />They can either let Matt know about an apartment or continue to
<br />spread the word via their own unique urls. The process continues
<br />until Matt finds his apartment, at which point every person in<br />
the chain is identified by his or her url and rewarded with<br />
a share of the $100 CrowdForce Challenge.</span><br /><br />

